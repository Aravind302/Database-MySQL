-- INNER JOIN 
SELECT ID1,NAME1,SALARY,STATUSS FROM EMPDETAILS INNER JOIN EMPSTATUS
ON EMPDETAILS.ID1=EMPSTATUS.ID2;


-- LEFT JOIN 

SELECT ID1,NAME1,SALARY ,STATUSS FROM EMPDETAILS LEFT JOIN EMPSTATUS ON EMPDETAILS.ID1=EMPSTATUS.ID2;

-- RIGHT JOIN

SELECT ID1,NAME1,SALARY ,STATUSS FROM EMPDETAILS RIGHT JOIN EMPSTATUS ON EMPDETAILS.ID1=EMPSTATUS.ID2;

-- OUTER JOIN

SELECT ID1,NAME1,SALARY ,STATUSS FROM EMPDETAILS LEFT JOIN EMPSTATUS ON EMPDETAILS.ID1=EMPSTATUS.ID2 UNION 
SELECT ID1,NAME1,SALARY ,STATUSS FROM EMPDETAILS RIGHT JOIN EMPSTATUS ON EMPDETAILS.ID1=EMPSTATUS.ID2;


